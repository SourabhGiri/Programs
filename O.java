class O 
{
	public static void main(String[] args) 
	{
		// Take two variable and store in two different variable
		int a = 10;
		int b = 23;

		// Add these values and store the sum in new variable
		int s = a+b;

		// Print the sum 
		System.out.println(s);

		// Print the "sum is  _"
		System.out.println("Sum is "+ s);

		// Print the "sum of a and b is sum"
		System.out.println("Sum of "+a+" and "+b+"is"+s);
	}
}