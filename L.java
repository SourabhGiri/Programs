class L 
{
	public static void main(String[] args) 
	{
		char ch = 'B';				
		System.out.println(ch);		//B		//Printing the value of character ch 
		ch = 'C';
		System.out.println(ch);		//C		//Printing the value of character ch
		ch = 'D';
		ch = 'E';
		System.out.println(ch);		//E		//Printing the value of character ch

		int a = 10;					//Declaring & Initialzing variable
		int b = 20;					//New Declaring & Initialzing variable
		System.out.println(a);		//10  //Printing the value of variable a
		System.out.println(b);		//20  //Printing the value of variable b
	}
}
